// Generate src/data/spot-index.ts — the id → region map (plus per-region spot
// counts) that lets Saved, deep links, and shot progress resolve a spot's
// city WITHOUT loading every city's chunk (docs/SCALING.md breakpoint 1).
// Parsing is regex-simple on purpose; tests/unit/spot-index.test.ts diffs the
// committed index against the real modules, so any drift fails CI.
// Run after editing spot data: node scripts/build-spot-index.mjs
import { readFileSync, writeFileSync, readdirSync } from 'node:fs'

const SPOTS_DIR = 'src/data/spots'

const entries = []
const counts = {}
for (const file of readdirSync(SPOTS_DIR).filter((f) => f.endsWith('.ts')).sort()) {
  const region = file.replace(/\.ts$/, '')
  const src = readFileSync(`${SPOTS_DIR}/${file}`, 'utf8')
  // Spot entries are `id: '<id>', name:` — signature shots use `id: …, label:`
  // so the trailing `, name:` disambiguates.
  const ids = [...src.matchAll(/id: '([a-z0-9-]+)', name:/g)].map((m) => m[1])
  if (!ids.length) throw new Error(`no spots matched in ${file} — format drift?`)
  for (const id of ids) entries.push([id, region])
  counts[region] = ids.length
}

const dupes = entries.map(([id]) => id).filter((id, i, a) => a.indexOf(id) !== i)
if (dupes.length) throw new Error(`duplicate spot ids across regions: ${dupes.join(', ')}`)

const lines = entries.map(([id, region]) => `  '${id}': '${region}',`).join('\n')
const countLines = Object.entries(counts).map(([r, n]) => `  '${r}': ${n},`).join('\n')

writeFileSync(
  'src/data/spot-index.ts',
  `// AUTO-GENERATED by scripts/build-spot-index.mjs — do not edit by hand.
// Rebuild whenever spot data changes; spot-index.test.ts enforces freshness.
import type { RegionId } from './regions'

/** Which city each spot lives in — resolve ids without loading every chunk. */
export const SPOT_REGION: Record<string, RegionId> = {
${lines}
}

/** Spots per city — powers progress totals without loading spot data. */
export const REGION_SPOT_COUNT: Record<RegionId, number> = {
${countLines}
}
`,
)
console.log(`wrote src/data/spot-index.ts: ${entries.length} spots across ${Object.keys(counts).length} regions`)
